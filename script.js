document.addEventListener('DOMContentLoaded', () => {
    const langBtns = document.querySelectorAll('.lang-btn');
    const elementsToTranslate = document.querySelectorAll('[data-i18n]');
    let currentLang = 'es';

    langBtns.forEach(btn => {
        btn.addEventListener('click', (e) => {
            langBtns.forEach(b => b.classList.remove('active'));
            e.target.classList.add('active');

            currentLang = e.target.getAttribute('data-lang');
            elementsToTranslate.forEach(el => {
                const key = el.getAttribute('data-i18n');
                if (dictionary[currentLang] && dictionary[currentLang][key]) {
                    el.innerHTML = dictionary[currentLang][key];
                }
            });
            document.documentElement.lang = currentLang;
            updateChartLang();
        });
    });

    const radarCtx = document.getElementById('radarChart');
    let radarChartObj = null;
    let gapChartObj = null;
    let barChartObj = null;

    if (radarCtx) {
        const ctx = radarCtx.getContext('2d');
        const gradient = ctx.createLinearGradient(0, 0, 0, 400);
        gradient.addColorStop(0, 'rgba(255, 130, 1, 0.4)');
        gradient.addColorStop(1, 'rgba(0, 0, 0, 0.1)');

        radarChartObj = new Chart(ctx, {
            type: 'radar',
            data: {
                labels: dpiRadarData.map(d => d.nameEs),
                datasets: [{
                    label: 'DPI UMO',
                    data: dpiRadarData.map(d => d.value),
                    backgroundColor: gradient,
                    borderColor: '#000000',
                    pointBackgroundColor: '#FF8201',
                    pointBorderColor: '#fff',
                    borderWidth: 2
                }]
            },
            options: {
                responsive: true,
                maintainAspectRatio: false,
                scales: {
                    r: {
                        angleLines: { color: 'rgba(0,0,0,0.1)' },
                        grid: { color: 'rgba(0,0,0,0.1)' },
                        pointLabels: {
                            color: '#000000',
                            font: { size: 10, family: "'Work Sans', sans-serif", weight: 'bold' }
                        },
                        ticks: {
                            backdropColor: 'transparent',
                            color: '#6F6F6F',
                            stepSize: 0.1,
                            min: 0,
                            max: 1
                        }
                    }
                },
                plugins: { legend: { display: false } }
            }
        });
    }

    const gapCtx = document.getElementById('gapChart');
    if (gapCtx) {
        const ctxGap = gapCtx.getContext('2d');
        gapChartObj = new Chart(ctxGap, {
            type: 'bar',
            data: {
                labels: dpiGapData.map(d => d.label),
                datasets: [
                    {
                        label: 'Obtenido',
                        backgroundColor: '#FF8201',
                        data: dpiGapData.map(d => d.obs)
                    },
                    {
                        label: 'Máximo',
                        backgroundColor: '#000000',
                        data: dpiGapData.map(d => d.max)
                    }
                ]
            },
            options: {
                responsive: true,
                maintainAspectRatio: false,
                plugins: { legend: { position: 'top' } },
                scales: {
                    y: { beginAtZero: true, max: 0.5 }
                }
            }
        });
    }

    const marketCtx = document.getElementById('marketChart');
    if (marketCtx) {
        const ctx2 = marketCtx.getContext('2d');
        barChartObj = new Chart(ctx2, {
            type: 'bar',
            data: {
                labels: ['Missouri', 'Oklahoma'],
                datasets: [{
                    label: 'Operaciones Agrícolas (NASS)',
                    backgroundColor: '#FF8201',
                    data: [85500, 69700]
                }]
            },
            options: {
                responsive: true,
                maintainAspectRatio: false,
                plugins: { legend: { position: 'bottom' } }
            }
        });
    }

    const updateChartLang = () => {
        if (radarChartObj) {
            radarChartObj.data.labels = currentLang === 'es' ?
                dpiRadarData.map(d => d.nameEs) :
                dpiRadarData.map(d => d.nameEn);
            radarChartObj.update();
        }
    };

    const menuToggle = document.querySelector('.menu-toggle');
    const navLinksContainer = document.querySelector('.nav-links');
    if (menuToggle && navLinksContainer) {
        menuToggle.addEventListener('click', () => {
            const isVisible = navLinksContainer.style.display === 'flex';
            navLinksContainer.style.display = isVisible ? 'none' : 'flex';
            if (!isVisible) {
                navLinksContainer.style.flexDirection = 'column';
                navLinksContainer.style.position = 'absolute';
                navLinksContainer.style.top = '90px';
                navLinksContainer.style.left = '0';
                navLinksContainer.style.width = '100%';
                navLinksContainer.style.background = '#000000';
                navLinksContainer.style.padding = '1rem 0';
            }
        });
    }
});
